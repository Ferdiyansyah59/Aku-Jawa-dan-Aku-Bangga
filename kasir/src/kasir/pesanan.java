    public void tambahData(){
        String query = "INSERT INTO pesanan (idpesanan, idmenu, idpelanggan, jumlah, iduser) VALUES (?,?,?,?,?)";
        try{
            stm = con.prepareStatement(query);
            stm.setString(1, idPesanan.getText());
            stm.setString(2, idMenu.getText());
            stm.setString(3, idPelanggan.getText());
            stm.setString(4, jumlah.getText());
            stm.setString(5, id);
            stm.executeUpdate();
            adaRecord = false;
        }catch(SQLException e){
            System.err.println("Eror di tambah pesanan "+e);
        }
        tampil();
    }
    
    public void tampilDataForm(){
        String query = "SELECT*FROM pesanan WHERE idpesanan=?";
        try{
            stm = con.prepareStatement(query);
            stm.setString(1, idPesanan.getText());
            res = stm.executeQuery();
            if(res.next()){
                idMenu.setText(res.getString("idmenu"));
                idPelanggan.setText(res.getString("idpelanggan"));
                jumlah.setText(res.getString("jumlah"));
                adaRecord = true;
            }else{
                idMenu.setText("");
                idPelanggan.setText("");
                jumlah.setText("");
            }
        }catch(SQLException e){
            System.err.println("Eror di tampilDataForm "+e);
        }
    }
    
    public void editData(){
        String query = "UPDATE pesanan SET idmenu=?, idpelanggan=?, jumlah=?, iduser=? WHERE idpesanan=?";
        try{
            stm = con.prepareStatement(query);
            stm.setString(1, idMenu.getText());
            stm.setString(2, idPelanggan.getText());
            stm.setString(3, jumlah.getText());
            stm.setString(4, id);
            stm.setString(5, idPesanan.getText());
            stm.executeUpdate();
        }catch(SQLException e){
            System.err.println("Eror di Edit "+e);
        }
    }
    
