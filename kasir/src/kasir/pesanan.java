    public Object[][] getDataPesanan(){
        Object[][] data = null;
        String query = "SELECT pesanan.idpesanan, menu.namamenu, pelanggan.namapelanggan, pesanan.jumlah FROM pesanan, menu, pelanggan WHERE menu.idmenu = pesanan.idmenu AND pelanggan.idpelanggan = pesanan.idpelanggan ORDER  BY idpesanan";
        try{
            stm = con.prepareStatement(query);
            res = stm.executeQuery();
            res.last();
            int baris = res.getRow();
            res.beforeFirst();
            data = new Object[baris][4];
            int i = 0;
            while(res.next()){
                data[i][0] = res.getString("idpesanan");
                data[i][1] = res.getString("namamenu");
                data[i][2] = res.getString("namapelanggan");
                data[i][3] = res.getString("jumlah");
                i++;
            }
        }catch(SQLException e){
            System.err.println("Erordi get Data "+e);
        }
        return data;
    }
    
