    private void inKodeKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_inKodeKeyReleased
        String qry = "SELECT*FROM mapel WHERE kode=?";
        try{
            pstmt = con.prepareStatement(qry);
            pstmt.setString(1, inKode.getText());
            res = pstmt.executeQuery();
            if(res.next()){
                inMapel.setText(res.getString("mapel"));
                if(res.getString("kelompok").equals("Muatan Nasional")){
                    inKelompok.setSelectedIndex(1);
                }else if(res.getString("kelompok").equals("Muatan Lokal")){
                    inKelompok.setSelectedIndex(2);
                }else if(res.getString("kelompok").equals("Produktif RPL")){
                    inKelompok.setSelectedIndex(3);
                }else if(res.getString("kelompok").equals("Produktif Tata Boga")){
                    inKelompok.setSelectedIndex(4);
                }else if(res.getString("kelompok").equals("Produktif Tata Busana")){
                    inKelompok.setSelectedIndex(5);
                }else if(res.getString("kelompok").equals("Produktif Perhotelan")){
                    inKelompok.setSelectedIndex(6);
                }else if(res.getString("kelompok").equals("Produktif UPW")){
                    inKelompok.setSelectedIndex(7);
                }
                adaRecord = true;
            }else{
                inMapel.setText("");
                inKelompok.setSelectedIndex(0);
                adaRecord = false;
            }
        }catch(SQLException e){
            System.err.println(e.getMessage());
        }
    }//GEN-LAST:event_inKodeKeyReleased

