    public void hapus(){
        if(adaRecord){
            int pilih = JOptionPane.showConfirmDialog(null, "Data Mapel " 
                    + inMapel.getText() + " Akan dihapus?", "HAPUS MAPEL!",JOptionPane.YES_NO_OPTION);
            if(pilih == JOptionPane.YES_OPTION){
                String qry = "DELETE FROM mapel WHERE kode=?";
                try{
                    pstmt = con.prepareStatement(qry);
                    pstmt.setString(1, inKode.getText());
                    pstmt.executeUpdate();
                    tampil();
                    adaRecord = false;
                }catch(SQLException e){
                    System.err.println(e);
                }
            }
        }
    }
    

