    public void simpanData(){
        String qry;
        if(adaRecord){
            qry = "UPDATE mapel SET mapel=?, kelompok=? WHERE kode=?";         
        }else{
            qry = "INSERT INTO mapel (mapel, kelompok, kode) VALUES (?,?,?)";  
        }
        
        try{
            pstmt = con.prepareStatement(qry);
            pstmt.setString(1, inMapel.getText());
            pstmt.setString(2, inKelompok.getSelectedItem().toString());
            pstmt.setString(3, inKode.getText());
            pstmt.executeUpdate();
            tampil();
        }catch(SQLException e){
            System.err.println(e.getMessage());
        }
    }
    
    
    
